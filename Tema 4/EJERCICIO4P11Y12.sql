CREATE TABLE AULA (
    IDAULA NUMBER(1) NOT NULL,
    NOMBRE VARCHAR(40) NOT NULL,
    TIPO VARCHAR(20) NOT NULL,
    CONSTRAINT PK_AULA PRIMARY KEY(IDAULA)
);

CREATE TABLE CURSOAULA (
    IDCURSO NUMBER(1) NOT NULL,
    IDAULA NUMBER(1) NOT NULL,
    CONSTRAINT PK_CURSOAULA PRIMARY KEY (IDCURSO, IDAULA),
    CONSTRAINT FK_CURSO FOREIGN KEY (IDCURSO) REFERENCES CURSO(IDCURSO),
    CONSTRAINT FK_AULA FOREIGN KEY (IDAULA) REFERENCES AULA(IDAULA)
);

