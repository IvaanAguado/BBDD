--a
SELECT
	EMPLEADO.DNI
FROM 
	EMPLEADO
WHERE
	EMPLEADO.CIUDAD IN ('Barcelona','Madrid','Sevilla','Málaga')
AND
	EMPLEADO.NOMBRE NOT IN('Antonio','Luis')
AND
	EMPLEADO.AP1 = 'García';


--b
SELECT
	EMPLEADO.DNI
FROM
	EMPLEADO
WHERE
	EMPLEADO.CIUDAD != ('Sevilla')
AND
	EMPLEADO.CIUDAD IN('Barcelona','Madrid')
AND
	EMPLEADO.NOMBRE LIKE 'M%';


--c
SELECT
	EMPLEADO.DNI
FROM
	EMPLEADO
WHERE
	EMPLEADO.CIUDAD IN('Sevilla')
AND
	EMPLEADO.CIUDAD NOT IN('Barcelona','Madrid')
AND
	EMPLEADO.NOMBRE NOT LIKE 'M%';


--d
SELECT
	EMPLEADO.NOMBRE,EMPLEADO.AP1
FROM
	EMPLEADO.CIUDAD 
WHERE
	IN ('Valencia','Vizcaya')
OR
	EMPLEADO.CIUDAD LIKE '%a'
ORDER BY EMPLEADO.AP1 DESC;


--e
SELECT
	EMPLEADO.DNI
FROM
	EMPLEADO
WHERE
	EMPLEADO.CIUDAD = 'Barcelona'
AND
	EMPLEADO.NOMBRE IN ('Antonio','Luis','Iván','Pedro')
AND
	EMPLEADO.AP1 IN('García','Cantos');
OR
	EMPLEADO.AP2 IN('García','Cantos');


--f
SELECT
	EMPLEADO.NOMBRE,EMPLEADO.AP1,EMPLEADO.AP2
FROM
	EMPLEADO
WHERE
	EMPLEADO.DNI LIKE '%_4_%'
AND
	EMPLEADO.NOMBRE IN ('Rosa','Luis','Ramón','Antonio')
ORDER BY EMPLEADO.AP1 DESC;


--g
SELECT
	EMPLEADO.DNI
FROM
	EMPLEADO
WHERE
	EMPLEADO.NOMBRE LIKE('Antonio')
AND
	LOWER(EMPLEADO.AP1) NOT LIKE '%c%'
AND
	EMPLEADO.AP2 IN('García','Gómez','Sánchez')
ORDER BY EMPLEADO.AP2 DESC;


--h
