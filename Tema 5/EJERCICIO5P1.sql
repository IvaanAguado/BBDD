SELECT
	CICLOFORM.NOMBRE
FROM 
	CICLOFORM
WHERE
	TIPO = 'GRADO SUPERIOR';


SELECT
	DATOSPERSONALES.NOMBRE, DATOSPERSONALES.AP1, DATOSPERSONALES.AP2, DATOSPERSONALES.DIRECCION
FROM 
	DATOSPERSONALES, PROFESOR
WHERE
	PROFESOR.DNI=DATOSPERSONALES.DNI;


SELECT DISTINCT
	CICLOFORM.NOMBRE
FROM 
	CICLOFORM, ASIGNATURA
WHERE
	CICLOFORM.CODCICLO=ASIGNATURA.CODCICLO
AND
	NUMHORAS BETWEEN 150 AND 250;


SELECT
	DATOSPERSONALES.NOMBRE, PROFESOR.CUENTA
FROM 
	DATOSPERSONALES, PROFESOR
WHERE
	PROFESOR.DNI=DATOSPERSONALES.DNI;


SELECT
	DATOSPERSONALES.DNI
FROM 
	DATOSPERSONALES, MATRICULA
WHERE
	MATRICULA.DNI=DATOSPERSONALES.DNI 
AND
	CODASIG= '3';


SELECT
	IDIOMASPROF.DNI 
FROM 
	IDIOMASPROF
WHERE
	CODIDIOMA = '30';


SELECT 
	ASIGNATURA.NOMBRE 
FROM 
	ASIGNATURA 
WHERE 
	ASIGNATURA.DNI LIKE '89621477%';


